"""12 кинематографических ракурсов для режима ВИДЕОГРАФИЯ."""
import math


def _l(a, b, t):
    return (a[0]+(b[0]-a[0])*t, a[1]+(b[1]-a[1])*t, a[2]+(b[2]-a[2])*t)


def _orbit(cx, cy, z, r, a0, a1, t):
    a = a0 + (a1-a0)*t
    return (cx + math.cos(a)*r, cy + math.sin(a)*r, z)


